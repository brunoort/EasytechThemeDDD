        public override bool ValidateUser(string username, string password)
        {
            string srvr = ConfigurationManager.AppSettings["PartySmart_AD"].ToString();
            bool authenticated = false;

            try
            {
                DirectoryEntry entry = new DirectoryEntry(srvr, username, password);
                object nativeObject = entry.NativeObject;
                authenticated = true;

                AuthAD auth = new AuthAD();
                var autorizadoAD = auth.GetGroups(username, password);

            }
            catch (DirectoryServicesCOMException cex)
            {
                //not authenticated; reason why is in cex
            }
            catch (Exception ex)
            {
                //not authenticated due to some other exception [this is optional]
            }

            return authenticated;
        }

